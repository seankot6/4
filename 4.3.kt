fun main() {
    print("Введите числовую оценку (1-5): ")
    val score = readLine()!!.toInt()

    val grade = when (score) {
        5 -> "Отлично"
        4 -> "Хорошо"
        3 -> "Удовлетворительно"
        2 -> "Неудовлетворительно"
        1 -> "на кол"
        else -> "Некорректная оценка"
    }

    println("Оценка: $grade")
}
