fun main() {
    print("Введите страну (США, Россия, Япония, Германия, Франция, Китай): ")
    val country = readLine()!!.toString()

    val nationality = when (country.uppercase()) {
        "США" -> "пендос"
        "РОССИЯ" -> "Россиянин"
        "ЯПОНИЯ" -> "анимешка"
        "ГЕРМАНИЯ" -> "фашист"
        "ФРАНЦИЯ" -> "наполеон"
        "КИТАЙ" -> "Китаеза"
        else -> "Неизвестная национальность."
    }

    println("Национальность: $nationality")
}
